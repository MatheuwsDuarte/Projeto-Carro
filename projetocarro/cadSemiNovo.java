    public void cadastrar(){
        s = new SemiNovo();
        
        try{    
            s.setAno(Integer.parseInt(txtAnocadSemiNovo.getText()));
            s.setSerie(Integer.parseInt(txtSeriecadSemiNovo.getText()));
            s.setValor(Double.parseDouble(txtValorcadSemiNovo.getText()));
            s.setModelo(txtModelocadSemiNovo.getText());
            s.setMarca(txtMarcacadSemiNovo.getText());
            s.setQm(Integer.parseInt(txtQmcadSemiNovo.getText()));
            s.setGarantia(txtGarantiacadSemiNovo.getText());
        
        s = GerSemiNovo.getGerSemiNovo().cadSemiNovo(s);
        
        if(s != null){
            JOptionPane.showMessageDialog(
                    null,
                    " Carro seminovo cadastrado com sucesso ",
                    "Cadastro Carro SemiNovo",
                    JOptionPane.PLAIN_MESSAGE
                );
            limpar();
        }
        else{
                    JOptionPane.showMessageDialog(
                    null,
                    "JÃ¡ existe um Carro com esse nÃºmero de Serie.",
                    "Cadastro Carro SemiNovo",
                    JOptionPane.ERROR_MESSAGE
                );
        }
    }
        catch(NumberFormatException nfe){
                JOptionPane.showMessageDialog(
                    null,
                    "O nÃºmero de serie deve ser um int",
                    "Erro de preenchimento",
                    JOptionPane.ERROR_MESSAGE
                );
         }   
    }
     
